// Task 1: Variables //
let employeeName = "Master Chief";
const employeeID = 117;
var isActive = true;

console.log(employeeName);  // Master Chief
console.log(typeof employeeName);  // string
console.log(employeeID);    // 117
console.log(typeof employeeID);    // number
console.log(isActive);      // true
console.log(typeof isActive);      // boolean

// Task 2: Primitive Data Types //
let productName = "Halo 3";
const productPrice = 59.99;
var isAvailable = true;

console.log(productName);  // Halo 3
console.log(typeof productName);  // string
console.log(productPrice);    // 59.99
console.log(typeof productPrice);    // number
console.log(isAvailable);      // true
console.log(typeof isAvailable);      // boolean

// Task 3: Number Data Type //
let accountBalance = 2000;

accountBalance += 500;
console.log(accountBalance);  // 2500

accountBalance -= 500;
console.log(accountBalance);  // 2000

accountBalance *= 1.05;
console.log(accountBalance);  // 2100

accountBalance /= 2;
console.log(accountBalance);  // 1050