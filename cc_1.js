// Task 1: Variables //
let employeeName = "Master Chief";
const employeeID = 117;
var isActive = true;

console.log(employeeName);  // Master Chief
console.log(typeof employeeName);  // string)
console.log(employeeID);    // 117
console.log(typeof employeeID);    // number
console.log(isActive);      // true
console.log( typeof isActive);      // boolean

